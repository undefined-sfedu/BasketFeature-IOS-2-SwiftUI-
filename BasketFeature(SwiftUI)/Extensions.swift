import Foundation
import SwiftUI

//MARK: - Extenstion For Color
extension Color{
    public static var customOrange: Color{
        Color.init(uiColor: UIColor(red: 240/256, green: 165/256, blue: 0/256, alpha: 1))
    }
    
    public static var customGray: Color{
        Color.init(uiColor: UIColor(red: 174/256, green: 174/256, blue: 174/256, alpha: 1))
    }
}
